// Logical operator

/**  
 * And - &&
 * Or - ||
 * Not - !
*/

var a = 1, b = 2, c = 3;

if (a > 0 && b > 0 && c > 0) {
    console.log(true);
}
else {
    console.log(false);
}

if (a > 0 || b < 0 || c < 0) {
    console.log(true);
}
else {
    console.log(false);
}

if (!(a > 0)) {
    console.log(true);
}
else {
    console.log(false);
}



