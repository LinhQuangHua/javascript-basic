// Switch in Javascript

var date = 9;

switch (date) {
    case 2:
        console.log("monday");
        break;
    case 3:
        console.log("tuesday");
        break;
    case 4:
        console.log("wednesday");
        break;
    case 5:
        console.log("thursday");
        break;
    case 6:
        console.log("friday");
        break;
    case 7:
        console.log("saturday");
        break;
    case 8:
        console.log("sunday");
        break;
    default:
        console.log("I don't know");
}

